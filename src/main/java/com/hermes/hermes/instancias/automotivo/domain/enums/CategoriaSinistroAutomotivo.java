package com.hermes.hermes.instancias.automotivo.domain.enums;

/**
 * Categorias de sinistros automotivos.
 */
public enum CategoriaSinistroAutomotivo {
    /**
     * Acidentes com colisão entre veículos
     */
    COLISAO,
    
    /**
     * Roubo ou furto do veículo
     */
    ROUBO_FURTO,
    
    /**
     * Incêndio no veículo
     */
    INCENDIO,
    
    /**
     * Danos causados por fenômenos naturais
     */
    DANOS_NATURAIS,
    
    /**
     * Pane mecânica do veículo
     */
    PANE_MECANICA,
    
    /**
     * Atos de vandalismo
     */
    VANDALISMO
}